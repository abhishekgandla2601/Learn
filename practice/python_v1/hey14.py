# operators

# arithmetic operators
a = 10
b = 20

print("Addition:", a+b)
print("Subtraction:", b-a)
print("Multiplication:", a*b)
print("Division:", b/a)
print("Modulus:", b%a)
print("Exponentiation:", a**2)
print("Floor Division:", b//a)

